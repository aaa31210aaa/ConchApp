package bean;


public class FormBean {
    //路段
    private String section;
    //爆落量
    private String explosionVolume;
    //出矿量
    private String oreOutput;
    //存矿量
    private String saveOre;
    //转场量
    private String transitVolume;
    //高钙
    private String calcium;
    //高硅
    private String silicon;
    //高镁
    private String magnesium;
    //泥夹石
    private String clayStone;


    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }

    public String getExplosionVolume() {
        return explosionVolume;
    }

    public void setExplosionVolume(String explosionVolume) {
        this.explosionVolume = explosionVolume;
    }

    public String getOreOutput() {
        return oreOutput;
    }

    public void setOreOutput(String oreOutput) {
        this.oreOutput = oreOutput;
    }

    public String getSaveOre() {
        return saveOre;
    }

    public void setSaveOre(String saveOre) {
        this.saveOre = saveOre;
    }

    public String getTransitVolume() {
        return transitVolume;
    }

    public void setTransitVolume(String transitVolume) {
        this.transitVolume = transitVolume;
    }

    public String getCalcium() {
        return calcium;
    }

    public void setCalcium(String calcium) {
        this.calcium = calcium;
    }

    public String getSilicon() {
        return silicon;
    }

    public void setSilicon(String silicon) {
        this.silicon = silicon;
    }

    public String getMagnesium() {
        return magnesium;
    }

    public void setMagnesium(String magnesium) {
        this.magnesium = magnesium;
    }

    public String getClayStone() {
        return clayStone;
    }

    public void setClayStone(String clayStone) {
        this.clayStone = clayStone;
    }
}
